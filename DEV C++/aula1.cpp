#include <stdio.h>

int main (){
	printf ("oi, tudo bem ? tenho x anos e programo. \n");
	
	printf("valor inteiro: %d \n", 10); // d para numeros inteiros
	
	printf ("valor real: %f \n", 3.141592); // f para numeros reais
	
	printf ("valor real com apenas duas casas: %.2f \n", 55.43256); //.2f para 2 casa depois da virgula

	printf ("dado de texto: %c \n", 'a'); // c para caractere letra 
	
	printf ("testando texto: %s \n", "brasil" ); // s para palavras ou textos
}



